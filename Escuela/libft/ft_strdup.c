#include "libft.h"

size_t	ft_strlen(const char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

char	*ft_strdup(const char *s)
{
	char *ret;

	if (!(ret = malloc(ft_strlen(s) * sizeof(char))))
		return (0);
	ret = (char*)s;
	return (ret);
}
