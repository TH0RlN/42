#include "ft_printf.h"

char	*ft_strchr(const char *s, int c)
{
	int len;

	len = ft_strlen(s) - 1;
	if (c == 0)
		return ((char *)s + len + 1);
	while (len >= 0)
	{
		if (s[len] == (char)c)
			return ((char *)s + len);
		len--;
	}
	return (0);
}

size_t	ft_strlen(const char *str)
{
	size_t i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}
